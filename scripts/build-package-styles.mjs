import fs from 'node:fs/promises';
import path from 'node:path';

const projectRoot = process.cwd();
const pandaStylesPath = path.join(projectRoot, 'styled-system', 'styles.css');
const katexStylesPath = path.join(projectRoot, 'node_modules', 'katex', 'dist', 'katex.min.css');
const katexFontsSourcePath = path.join(projectRoot, 'node_modules', 'katex', 'dist', 'fonts');
const packageFontsPath = path.join(projectRoot, 'fonts');
const packageStylesPath = path.join(projectRoot, 'styles.css');
const packageLiteStylesPath = path.join(projectRoot, 'styles-lite.css');

const [pandaStyles, katexStyles] = await Promise.all([
  fs.readFile(pandaStylesPath, 'utf8'),
  fs.readFile(katexStylesPath, 'utf8'),
]);

const packageStyles = [
  '/* Generated by scripts/build-package-styles.mjs. */',
  '/* Includes the package default Panda CSS and KaTeX runtime styles. */',
  pandaStyles.trim(),
  katexStyles.trim(),
  '',
].join('\n\n');

const packageLiteStyles = [
  '/* Generated by scripts/build-package-styles.mjs. */',
  '/* Includes the package default Panda CSS without KaTeX runtime styles. */',
  pandaStyles.trim(),
  '',
].join('\n\n');

await fs.writeFile(packageStylesPath, packageStyles, 'utf8');
await fs.writeFile(packageLiteStylesPath, packageLiteStyles, 'utf8');
await fs.rm(packageFontsPath, { force: true, recursive: true });
await fs.cp(katexFontsSourcePath, packageFontsPath, { recursive: true });
